    @Override
    public void update(Observable o, Object arg) {
        CaballoCarrera c =(CaballoCarrera) o;
        int porcent=(int) arg;
        switch (c.getCaballo()) {
            case "1":
                this.p1.setValue(porcent);
                break;
 
            case "2":
                this.p2.setValue(porcent);
                break;
 
            case "3":
                this.p3.setValue(porcent);
                break;
 
            case "4":
                this.p4.setValue(porcent);
                break;
 
        }
 
        if(porcent>=100){
           terminar();
           this.jButton1.setEnabled(true);
           this.ganador.setText("Caballo "+c.getCaballo());
        }
         
         
    
    }

